package proyectobiblioteca;

/**
 *
 * @author VictorHugo
 */
public abstract class Lectura extends Item{
    private String numeroDePaginas;
    private String fecha;

    /**
     * @return the numeroDePaginas
     */
    public String getNumeroDePaginas() {
        return numeroDePaginas;
    }

    /**
     * @param numeroDePaginas the numeroDePaginas to set
     */
    public void setNumeroDePaginas(String numeroDePaginas) {
        this.numeroDePaginas = numeroDePaginas;
    }

    /**
     * @return the fecha
     */
    public String getFecha() {
        return fecha;
    }

    /**
     * @param fecha the fecha to set
     */
    public void setFecha(String fecha) {
        this.fecha = fecha;
    }
    
}
